#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t	i;
	size_t	j;
	size_t	size;

	size = ft_strlen((char *)src) + ft_strlen((char *)dest);
	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (i <= size && src[j] != '\0' && j < n)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
